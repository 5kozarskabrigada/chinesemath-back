export async function createUser(req, res) {
  let { username, email, first_name, last_name, password, role } = req.body;
  const normalizedFirstName = first_name?.trim();
  const normalizedLastName = last_name?.trim();
  
  // Validate required fields
  if (!normalizedFirstName || !normalizedLastName) {
    return res.status(400).json({ error: "First name and last name are required" });
  }

  // Auto-generate username and password if not provided
  const autogenerated = {};
  if (!username) {
    username = generateUsername(normalizedFirstName, normalizedLastName);
    autogenerated.username = true;
  }
  if (!password) {
    password = generatePassword();
    autogenerated.password = true;
  }

  try {
    const normalizedEmail = email?.trim() ? email.trim().toLowerCase() : null;
    const hash = await bcrypt.hash(password, 12);

    for (let attempt = 0; attempt < 8; attempt++) {
      try {
        const result = await pool.query(
          `INSERT INTO users (username, email, first_name, last_name, password_hash, role)
           VALUES ($1, $2, $3, $4, $5, $6)
           RETURNING id, username, email, first_name, last_name, role`,
          [username.trim(), normalizedEmail, normalizedFirstName, normalizedLastName, hash, role || "student"]
        );

        // Return both created user and plain text credentials if autogenerated
        const response = { user: result.rows[0] };
        if (autogenerated.username || autogenerated.password) {
          response.credentials = { username, password };
          response.autogenerated = autogenerated;
        }

        return res.status(201).json(response);
      } catch (err) {
        const isUniqueViolation = err.code === "23505";
        const isUsernameConflict =
          err.constraint === "users_username_key" || err.detail?.includes("(username)");

        if (isUniqueViolation && autogenerated.username && isUsernameConflict && attempt < 7) {
          username = generateUsername(normalizedFirstName, normalizedLastName);
          continue;
        }

        if (isUniqueViolation) {
          return res.status(409).json({ error: "Username or email already exists" });
        }

        throw err;
      }
    }
  } catch (err) {
    console.error("createUser error:", err);
    res.status(500).json({ error: "Internal server error" });
  }
}

