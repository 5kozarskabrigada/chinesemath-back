export async function createExam(req, res) {
  const { title, description, duration_minutes, questions } = req.body;
  const normalizedTitle = title?.trim();
  const safeQuestions = Array.isArray(questions) ? questions : [];

  if (!normalizedTitle) {
    return res.status(400).json({ error: "Title is required" });
  }

  const accessCode = Math.random().toString(36).slice(2, 8).toUpperCase();
  let client;

  try {
    client = await pool.connect();
    await client.query("BEGIN");

    const examResult = await client.query(
      `INSERT INTO exams (title, description, duration_minutes, total_questions, access_code, created_by)
       VALUES ($1, $2, $3, $4, $5, $6)
       RETURNING id`,
      [normalizedTitle, description?.trim() || "", duration_minutes || 60, safeQuestions.length, accessCode, req.user.id]
    );
    const examId = examResult.rows[0].id;

    for (let i = 0; i < safeQuestions.length; i++) {
      const q = safeQuestions[i];
      await client.query(
        `INSERT INTO questions (exam_id, question_number, question_text, options, correct_answer, explanation)
         VALUES ($1, $2, $3, $4, $5, $6)`,
        [examId, i + 1, q.question_text, JSON.stringify(q.options), q.correct_answer, q.explanation || ""]
      );
    }

    await client.query("COMMIT");
    res.status(201).json({ examId, accessCode });
  } catch (err) {
    if (client) {
      try {
        await client.query("ROLLBACK");
      } catch (rollbackErr) {
        console.error("createExam rollback error:", rollbackErr);
      }
    }
    console.error("createExam error:", err);
    res.status(500).json({ error: "Internal server error" });
  } finally {
    if (client) client.release();
  }
}

export async function updateExam(req, res) {
  const { examId } = req.params;
  const { title, description, duration_minutes, status, questions } = req.body;
  const hasQuestions = Array.isArray(questions);
  let client;

  try {
    client = await pool.connect();
    await client.query("BEGIN");

    await client.query(
      `UPDATE exams SET title = COALESCE($1, title), description = COALESCE($2, description),
       duration_minutes = COALESCE($3, duration_minutes), status = COALESCE($4, status),
       total_questions = COALESCE($5, total_questions), updated_at = NOW()
       WHERE id = $6 AND is_deleted = false`,
      [title, description, duration_minutes, status, hasQuestions ? questions.length : null, examId]
    );

    if (hasQuestions) {
      // Delete existing questions then re-insert
      await client.query(`DELETE FROM questions WHERE exam_id = $1`, [examId]);
      for (let i = 0; i < questions.length; i++) {
        const q = questions[i];
        await client.query(
          `INSERT INTO questions (exam_id, question_number, question_text, options, correct_answer, explanation)
           VALUES ($1, $2, $3, $4, $5, $6)`,
          [examId, i + 1, q.question_text, JSON.stringify(q.options), q.correct_answer, q.explanation || ""]
        );
      }
    }

    await client.query("COMMIT");
    res.json({ success: true });
  } catch (err) {
    if (client) {
      try {
        await client.query("ROLLBACK");
      } catch (rollbackErr) {
        console.error("updateExam rollback error:", rollbackErr);
      }
    }
    console.error("updateExam error:", err);
    res.status(500).json({ error: "Internal server error" });
  } finally {
    if (client) client.release();
  }
}

