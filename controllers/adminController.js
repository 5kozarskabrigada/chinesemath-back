export async function addStudentToClassroom(req, res) {
  const { classroomId } = req.params;
  const { userId } = req.body;

  if (!userId) {
    return res.status(400).json({ error: "User ID is required" });
  }

  try {
    // Check if classroom exists
    const classroomCheck = await pool.query(
      `SELECT id FROM classrooms WHERE id = $1 AND is_deleted = false`,
      [classroomId]
    );

    if (classroomCheck.rows.length === 0) {
      return res.status(404).json({ error: "Classroom not found" });
    }

    // Check if user exists and is a student
    const userCheck = await pool.query(
      `SELECT id, role FROM users WHERE id = $1 AND is_deleted = false`,
      [userId]
    );

    if (userCheck.rows.length === 0) {
      return res.status(404).json({ error: "User not found" });
    }

    if (userCheck.rows[0].role !== 'student') {
      return res.status(400).json({ error: "Only students can be added to classrooms" });
    }

    // Add student to classroom (or ignore if already exists)
    const result = await pool.query(
      `INSERT INTO classroom_members (classroom_id, user_id)
       VALUES ($1, $2)
       ON CONFLICT (classroom_id, user_id) DO NOTHING
       RETURNING *`,
      [classroomId, userId]
    );

    if (result.rows.length === 0) {
      return res.status(409).json({ error: "Student already in classroom" });
    }

    res.status(201).json({ message: "Student added to classroom successfully" });
  } catch (err) {
    console.error("addStudentToClassroom error:", err);
    res.status(500).json({ error: "Internal server error" });
  }
}

